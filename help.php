            <section id="general" class="section">
                <h2>1. Общее описание</h2>
                <p>Система предназначена для централизованного управления конфигурациями IP-телефонов. Телефоны обращаются к серверу по HTTP, получают файл конфигурации, сформированный на основе шаблона и параметров, заданных в профилях. Поддерживается:</p>
                <ul>
                    <li>Иерархия профилей с наследованием полей.</li>
                    <li>Автоматическое определение производителя (OUI) по MAC-адресу.</li>
                    <li>Привязка телефонов к профилям и подразделениям.</li>
                    <li>Переопределение параметров для конкретного телефона.</li>
                    <li>Правила преобразования значений в зависимости от шаблона (map/fixed).</li>
                    <li>Дефолтные шаблоны для неизвестных телефонов (опционально).</li>
                    <li>Логирование всех обращений.</li>
                </ul>
            </section>

            <!-- 2. Профили настроек -->
            <section id="profiles" class="section">
                <h2>2. Профили настроек</h2>
                <p><strong>Страница:</strong> <code>profiles.php</code></p>
                <p>Здесь создаются и редактируются профили настроек. Профиль — это набор полей (ключ-значение), которые будут подставлены в шаблон конфигурации. Ключи используются в шаблоне в виде <code>%ключ%</code>.</p>
                <h3>Возможности:</h3>
                <ul>
                    <li>Создание, редактирование, удаление профиля.</li>
                    <li>Клонирование профиля вместе со всеми полями.</li>
                    <li>У каждого профиля может быть родитель (поле <code>parent_id</code>) — тогда все поля родителя наследуются и могут быть переопределены.</li>
                </ul>
            </section>

            <!-- 3. Поля профилей и наследование -->
            <section id="profile-fields" class="section">
                <h2>3. Поля профилей и наследование</h2>
                <p><strong>Страница:</strong> <code>profile_fields.php?profile_id=...</code></p>
                <p>Управление полями конкретного профиля. Отображаются два раздела:</p>
                <ul>
                    <li><strong>Поля текущего профиля</strong> — созданные непосредственно в этом профиле. Их можно сортировать перетаскиванием, редактировать, удалять.</li>
                    <li><strong>Унаследованные поля</strong> — поля из родительских профилей. Для каждого такого поля доступна кнопка «Переопределить», которая создаёт поле в текущем профиле с тем же ключом (значение копируется из родителя, но может быть изменено).</li>
                </ul>
                <p>При добавлении нового поля проверяется уникальность ключа в пределах текущего профиля. Если ключ уже существует в родителях, выводится предупреждение.</p>
                <p>Для каждого поля (как текущего, так и унаследованного) есть иконка ⚙️ для перехода к настройке правил преобразования (<code>field_rules.php</code>).</p>
            </section>

            <!-- 4. Шаблоны телефонов -->
            <section id="templates" class="section">
                <h2>4. Шаблоны телефонов</h2>
                <p><strong>Страница:</strong> <code>templates.php</code></p>
                <p>Шаблоны привязаны к производителю (первые 6 символов MAC — OUI). Каждый шаблон содержит:</p>
                <ul>
                    <li>Название (для удобства).</li>
                    <li>OUI (например, <code>249AD8</code> для Yealink).</li>
                    <li>Содержимое шаблона (текст конфигурации с плейсхолдерами <code>%ключ%</code>).</li>
                    <li>Флаг <strong>Активировать шаблон по умолчанию</strong> — флаг который указывает нужно ли отдавать дефолтную конфигурацию неизвестным телефонам</li>
                    <li>Поле <strong>Содержимое шаблона по умолчанию</strong> — конфигурация которая будет передана телефону не добавленному по MAC, но если шаблон найден по OUI. По сути дефолтный конфиг для неизвестных телефонов.</li>
                </ul>
            </section>

            <!-- 5. Правила преобразования полей -->
            <section id="transform-rules" class="section">
                <h2>5. Правила преобразования полей</h2>
                <p><strong>Страница:</strong> <code>field_rules.php?field_key=...&profile_id=...</code></p>
                <p>Позволяет задать, как значение конкретного поля будет изменяться в зависимости от того, какой шаблон используется для телефона. Доступны два типа правил:</p>
                <ul>
                    <li><strong>Сопоставление (map)</strong> — преобразование по словарю (например, <code>0</code> → <code>1</code>, <code>1</code> → <code>3</code>). Если исходного значения нет в словаре, оно остаётся без изменений.</li>
                    <li><strong>Фиксированное значение (fixed)</strong> — полностью заменяет значение поля на указанное (например, всегда подставлять <code>pool.ntp.org</code>).</li>
                </ul>
                <p>Правила задаются для каждого шаблона отдельно. Если для шаблона правило не задано, используется исходное значение из профиля.</p>
            </section>

            <!-- 6. Телефоны -->
            <section id="phones" class="section">
                <h2>6. Телефоны</h2>
                <p><strong>Страница:</strong> <code>phones.php</code></p>
                <p>Список зарегистрированных телефонов. Для каждого отображается:</p>
                <ul>
                    <li>MAC-адрес (и под ним название шаблона, определённого по OUI).</li>
                    <li>IP-адрес последнего обращения.</li>
                    <li>Рабочее место (текстовое поле).</li>
                    <li>Привязанный профиль.</li>
                    <li>Подразделение.</li>
                    <li>Дата последнего обращения телефона к серверу автопровижинга</li>
                </ul>
                <p>Доступны фильтры: по подразделению (select2) и поиск по MAC или IP. Кнопка «Добавить телефон» ведёт на форму создания.</p>
            </section>

            <!-- 7. Переопределение полей для телефона -->
            <section id="phone-overrides" class="section">
                <h2>7. Переопределение полей для телефона</h2>
                <p><strong>Страница:</strong> <code>phone_edit.php?id=...</code></p>
                <p>При редактировании конкретного телефона можно:</p>
                <ul>
                    <li>Изменить MAC, рабочее место, профиль, подразделение.</li>
                    <li>Просмотреть все поля, доступные для данного профиля (с учётом наследования).</li>
                    <li>Переопределить значение любого поля — для этого создаётся запись в таблице <code>phone_overrides</code>. Приоритет имеют переопределённые значения.</li>
                </ul>
                <p>Поля разделены на две группы: обязательные (<code>is_required = 1</code>) и остальные. Переопределить можно любое поле, нажав на иконку редактирования.</p>
                <p>При создании телефона (если он ранее был в <code>Запросы</code>) можно передать MAC из запроса, тогда форма предзаполнится.</p>
            </section>

            <!-- 8. Запросы (неизвестные телефоны) -->
            <section id="requests" class="section">
                <h2>8. Запросы (неизвестные телефоны)</h2>
                <p><strong>Страница:</strong> <code>requests.php</code></p>
                <p>Сюда попадают телефоны, которые обращались к серверу, но не зарегистрированы в <code>phones</code>, при условии, что для их OUI существует шаблон. Если у шаблона включён флаг <code>default_template_is_enabled</code>, такому телефону будет отдан дефолтный конфиг (из <code>default_template_content</code> или основного шаблона) и он попадёт в список запросов. Если флаг выключен — возвращается ошибка 404.</p>
                <p>В таблице запросов отображаются MAC, OUI, IP, user-agent, шаблон, дата последнего обращения. Запись обновляется при каждом новом обращении (REPLACE).</p>
                <p>Действия:</p>
                <ul>
                    <li><strong>Создать телефон</strong> — открывает форму <code>phone_edit.php</code> с предзаполненным MAC, профилем (берётся из первого попавшегося, можно изменить) и подразделением. После сохранения запись из запросов автоматически удаляется.</li>
                    <li><strong>Отклонить</strong> — просто удаляет запись из <code>unknown_requests</code>.</li>
                </ul>
            </section>

            <!-- 9. Подразделения -->
            <section id="departments" class="section">
                <h2>9. Подразделения</h2>
                <p><strong>Страница:</strong> <code>departments.php</code></p>
                <p>Управление структурой подразделений (отделов). Поддерживается иерархия (parent_id) и сортировка (<code>sort_id</code>) на каждом уровне. Подразделения отображаются с отступами, показывающими вложенность.</p>
                <p>Используется для фильтрации телефонов и привязки телефонов к подразделению.</p>
            </section>

            <!-- 10. Логи -->
            <section id="logs" class="section">
                <h2>10. Логи</h2>
                <p><strong>Страница:</strong> <code>logs.php</code></p>
                <p>Журнал всех обращений к серверу provisioning. Записываются:</p>
                <ul>
                    <li>MAC-адрес (если удалось извлечь).</li>
                    <li>IP.</li>
                    <li>User-Agent.</li>
                    <li>Дополнительная информация (модель, прошивка, результат обработки).</li>
                    <li>Полный дамп запроса (сериализованный).</li>
                </ul>
                <p>Доступны фильтры: по MAC, IP, дате. Предусмотрена регулярная очистка старых записей (например, через cron удаляются логи старше 6 месяцев).</p>
            </section>

            <!-- 11. Авторизация -->
            <section id="auth" class="section">
                <h2>11. Авторизация</h2>
                <p>Доступ к интерфейсу управления защищён простой HTTP-авторизацией (логин/пароль заданы в файле <code>auth.php</code>). . Сессия сохраняется на время работы браузера.</p>
                <p>Страница <code>provision.php</code> (точка выдачи конфигов) открыта без авторизации, так как к ней обращаются телефоны.</p>
            </section>

